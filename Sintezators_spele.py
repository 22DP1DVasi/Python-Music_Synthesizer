from winsound import Beep
from time import sleep
import os

def main_func():        #galvenā funkcija
    os.system('cls')
    TitleScreen()       #titulekrāns

    start_command = str(input('Rakstiet komandu šeit: '))         #lietotājs ievada komandu
    count_help_command = 0                                #mainīgais, kas pārbauda, vai lietotājs atrodas palīdzības ekrānā

    while start_command != 'start':             #kamēr komanda nav 'start'
        if start_command == 'quit':             #ja komanda ir 'quit'
            os.system('cls')
            quit()                              #iziet no spēles
        elif start_command == 'help':           #ja komanda ir 'help'  
            if count_help_command == 0:         #ja lietotājs neatrodas palīdzības ekrānā   
                os.system('cls')
                sleep(1)
                Help_screen()
                count_help_command = 1
                start_command = str(input('Rakstiet komandu šeit: '))
            else:                               #ja lietotājs jau atrodas palīdzības ekrānā   
                start_command = str(input('Jūs jau esat uz palīdzības ekrāna! Rakstiet komandu šeit: '))
        else:                                   #ja komanda ir nepareiā
            os.system('cls')
            sleep(1)
            Wrong_Command_Screen()                    
            count_help_command = 0
            start_command = str(input('Rakstiet komandu šeit: '))

    os.system('cls')
    sleep(1)        
    Standart_game_screen()                      #spēles galvenais ekrāns
    
    notes = {'C': 262,                          #notis un to frekvences
             'D': 294,
             'E': 330,         
             'F': 349,
             'G': 392,
             'A': 440,
             'B': 494,
             ' ': 37}
    
    melodie = str(input('Uzrakstiet savu melodiju!: '))         #lietotājs ievada savu melodiju
    while melodie != 'quit':                                    #kamēr komanda nav 'quit'
        fin_notes_check = False                                 #notu pārbaudes mainīgais
        while fin_notes_check == False:
            for note_check in melodie:                          #operators pārbauda katru rakstzīmi virknē
                if note_check in notes:                         #ja nots ir vārdnīcā
                    fin_notes_check = True
                else:                                           #ja nots nav vārdnīcā
                    if melodie == 'quit':
                        os.system('cls')
                        quit()
                    else:
                        fin_notes_check = False
                        melodie = str(input('Jūs ievadījāt nepareizu noti! Mēģiniet vēlreiz! Uzrakstiet savu melodiju!: '))
                        break

        for note in melodie:                                    #visas notis tiek atskaņotas
            Play_animation(note)                                #mūzikas spēles animācija
            Beep(notes[note], 500)
        
        Standart_game_screen()                                  #spēles galvenais ekrāns
        melodie = str(input('Uzrakstiet savu melodiju!: '))

    os.system('cls')                                            #ja komanda ir 'quit', iziet no spēles
    quit()



